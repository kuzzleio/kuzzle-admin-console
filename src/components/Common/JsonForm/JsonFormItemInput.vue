<template>
  <div class="row input-field">
    <input
      :type="type"
      :value="value"
      :step="step"
      @input="update"
      @focus="display = true"
    />
    <label :class="{ active: value !== null }">{{ name }}</label>
  </div>
</template>

<script>
export default {
  name: 'JsonFormItemInput',
  props: {
    content: [Object, String, Number],
    name: String,
    type: String,
    step: Number,
    parent: String
  },
  data() {
    return {
      value: '',
      display: false
    }
  },
  watch: {
    content: 'initValue'
  },
  mounted() {
    this.initValue()
  },
  methods: {
    update(e) {
      this.$emit('update-value', { name: this.name, value: e.target.value })
    },
    initValue() {
      if (!this.content || !Object.keys(this.content).length) {
        return
      }

      if (this.parent) {
        if (this.content[this.parent] && this.content[this.parent][this.name]) {
          this.value = this.content[this.parent][this.name]
        }
      } else {
        this.value = this.content[this.name]
      }
    }
  }
}
</script>
